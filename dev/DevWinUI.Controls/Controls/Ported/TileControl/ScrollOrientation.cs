namespace DevWinUI;

public enum ScrollOrientation
{
    /// <summary>
    /// Scroll only Horizontally (and optimize the number of image used)
    /// </summary>
    Horizontal,

    /// <summary>
    /// Scroll only Vertically (and optimize the number of image used)
    /// </summary>
    Vertical,

    /// <summary>
    /// Scroll both Horizontally and vertically
    /// </summary>
    Both
}